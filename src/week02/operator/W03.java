package week02.operator;
// 2-2강의
public class W03 {
    // 비교연산자 예제
    public static void main(String[] args) {

        // 비교 연산자의 결과는 boolean 타입
        System.out.println(10 > 9);
        System.out.println(10 >= 9);
        System.out.println(10 < 9);
        System.out.println(10 <= 9);
        System.out.println(10 == 10);
        System.out.println(10 == 9);
        System.out.println(10 != 10);
        System.out.println(10 == 9);

    }
}
